    // -----------------------------------------------------------------------
    // Security regression tests (Issue 30 / S-9)
    // -----------------------------------------------------------------------

    /// Issue #30 (S-9): `from_bytes()` returns an unverified envelope
    /// (`is_verified() == false`).  `from_bytes_verified()` atomically
    /// parses and verifies — rejecting bad signatures and returning a
    /// verified envelope on success.
    #[test]
    fn sec_envelope_from_bytes_verified_gate() {
        let key_a = test_key_a();
        let key_b = test_key_b();
        let id_b = agent_id(&key_b.verifying_key());
        let attest = make_attestation();
        let now = 5000u64;

        // --- 1. create() produces a verified envelope ---
        let env = ExchangeEnvelope::create([0x42; 32], id_b, &attest, None, now, &key_a).unwrap();
        assert!(
            env.is_verified(),
            "create() must produce a verified envelope"
        );

        let bytes = env.to_bytes();

        // --- 2. from_bytes() produces an UN-verified envelope ---
        let raw = ExchangeEnvelope::from_bytes(&bytes);
        assert!(
            !raw.is_verified(),
            "from_bytes() must return an unverified envelope"
        );

        // --- 3. from_bytes_verified() rejects garbage signature ---
        let mut bad = bytes;
        bad[136] ^= 0xFF; // flip a signature byte
        let err = ExchangeEnvelope::from_bytes_verified(
            &bad,
            &id_b,
            None,
            &attest,
            None,
            &key_a.verifying_key(),
            now + 5,
            300,
        );
        assert!(
            err.is_err(),
            "from_bytes_verified must reject a tampered signature"
        );

        // --- 4. from_bytes_verified() accepts a valid envelope ---
        let verified = ExchangeEnvelope::from_bytes_verified(
            &bytes,
            &id_b,
            None,
            &attest,
            None,
            &key_a.verifying_key(),
            now + 5,
            300,
        )
        .expect("from_bytes_verified must accept a valid envelope");
        assert!(
            verified.is_verified(),
            "from_bytes_verified must return a verified envelope"
        );
        assert_eq!(verified.nonce, [0x42; 32]);
    }
