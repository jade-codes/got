// ---------------------------------------------------------------------------
// Signed Exchange Envelope — Phase 10, §10.6.
//
// Binds an attestation to a specific exchange with a specific peer.
// The envelope signature covers:
//   nonce ‖ peer_agent_id ‖ attestation_hash ‖ chain_root_hash ‖ timestamp
// (all fixed-width, no delimiters needed).
// ---------------------------------------------------------------------------

use ed25519_dalek::{Signer, SigningKey, Verifier, VerifyingKey};

use got_attest::serialise_for_signing;
use got_core::GeometricAttestation;

use crate::WireError;

/// Signed exchange envelope — wraps an attestation in exchange-specific bindings.
#[derive(Debug, Clone)]
pub struct ExchangeEnvelope {
    /// Random 32-byte nonce (generated by initiator, echoed by responder).
    pub nonce: [u8; 32],
    /// SHA-256 of the intended recipient's public key.
    pub peer_agent_id: [u8; 32],
    /// SHA-256 of the current attestation's `serialise_for_signing()` bytes.
    pub attestation_hash: [u8; 32],
    /// SHA-256 of chain[0]'s `serialise_for_signing()` bytes, or zeroes if no chain.
    pub chain_root_hash: [u8; 32],
    /// Unix UTC seconds when the envelope was created.
    pub timestamp: u64,
    /// Ed25519 signature over all fields above.
    pub signature: [u8; 64],
    /// Defence-in-depth: tracks whether `verify()` has been called successfully.
    /// Starts `false` from `from_bytes()` / `from_bytes_verified()`.
    /// Set to `true` only by a successful `verify()` call.
    /// `create()` also sets this to `true` (self-signed, implicitly verified).
    verified: bool,
}

/// Size of the signable portion: 32 + 32 + 32 + 32 + 8 = 136 bytes.
pub const SIGNABLE_SIZE: usize = 136;

/// Total serialised envelope size: 136 + 64 = 200 bytes.
pub const ENVELOPE_SIZE: usize = 200;

impl ExchangeEnvelope {
    /// Build the fixed-width byte array that is signed.
    pub fn signable_bytes(&self) -> [u8; SIGNABLE_SIZE] {
        let mut buf = [0u8; SIGNABLE_SIZE];
        buf[0..32].copy_from_slice(&self.nonce);
        buf[32..64].copy_from_slice(&self.peer_agent_id);
        buf[64..96].copy_from_slice(&self.attestation_hash);
        buf[96..128].copy_from_slice(&self.chain_root_hash);
        buf[128..136].copy_from_slice(&self.timestamp.to_le_bytes());
        buf
    }

    /// Create and sign an envelope.
    #[allow(clippy::too_many_arguments)]
    pub fn create(
        nonce: [u8; 32],
        peer_agent_id: [u8; 32],
        attestation: &GeometricAttestation,
        chain_anchor: Option<&GeometricAttestation>,
        timestamp: u64,
        signing_key: &SigningKey,
    ) -> Result<Self, WireError> {
        let attest_bytes = serialise_for_signing(attestation)?;
        let attestation_hash = got_core::sha256(&attest_bytes);

        let chain_root_hash = match chain_anchor {
            Some(anchor) => {
                let anchor_bytes = serialise_for_signing(anchor)?;
                got_core::sha256(&anchor_bytes)
            }
            None => [0u8; 32],
        };

        let mut envelope = Self {
            nonce,
            peer_agent_id,
            attestation_hash,
            chain_root_hash,
            timestamp,
            signature: [0u8; 64],
            verified: true, // self-signed → implicitly verified
        };

        let signable = envelope.signable_bytes();
        let sig = signing_key.sign(&signable);
        envelope.signature = sig.to_bytes();

        Ok(envelope)
    }

    /// Verify the envelope's signature and all bindings.
    ///
    /// * `expected_own_id` — our agent ID; must match `peer_agent_id`.
    /// * `expected_nonce` — for responses, the nonce we sent; `None` for requests.
    /// * `attestation` — the attestation payload; hash must match `attestation_hash`.
    /// * `chain_anchor` — first attestation in chain (if any); hash must match.
    /// * `signer_pk` — the sender's public key to verify the signature.
    /// * `now` — current Unix timestamp for freshness check.
    /// * `max_age_secs` — maximum allowed age of the envelope timestamp.
    #[allow(clippy::too_many_arguments)]
    pub fn verify(
        &self,
        expected_own_id: &[u8; 32],
        expected_nonce: Option<&[u8; 32]>,
        attestation: &GeometricAttestation,
        chain_anchor: Option<&GeometricAttestation>,
        signer_pk: &VerifyingKey,
        now: u64,
        max_age_secs: u64,
    ) -> Result<(), WireError> {
        // 1. Verify Ed25519 signature
        let signable = self.signable_bytes();
        let sig = ed25519_dalek::Signature::from_bytes(&self.signature);
        signer_pk
            .verify(&signable, &sig)
            .map_err(|_| WireError::EnvelopeSignatureInvalid)?;

        // 2. Check peer_agent_id matches our own ID
        if self.peer_agent_id != *expected_own_id {
            return Err(WireError::PeerIdMismatch {
                expected: hex_short(expected_own_id),
                got: hex_short(&self.peer_agent_id),
            });
        }

        // 3. Check nonce (for responses)
        if let Some(expected) = expected_nonce {
            if self.nonce != *expected {
                return Err(WireError::NonceMismatch);
            }
        }

        // 4. Check attestation hash
        let attest_bytes = serialise_for_signing(attestation)?;
        let actual_hash = got_core::sha256(&attest_bytes);
        if self.attestation_hash != actual_hash {
            return Err(WireError::AttestationHashMismatch);
        }

        // 5. Check chain root hash
        let expected_root = match chain_anchor {
            Some(anchor) => {
                let anchor_bytes = serialise_for_signing(anchor)?;
                got_core::sha256(&anchor_bytes)
            }
            None => [0u8; 32],
        };
        if self.chain_root_hash != expected_root {
            return Err(WireError::ChainRootHashMismatch);
        }

        // 6. Timestamp freshness — reject both too-old and future timestamps
        if self.timestamp > now {
            return Err(WireError::TimestampExpired {
                age_secs: 0,
                max_secs: max_age_secs,
            });
        }
        let age = now - self.timestamp;
        if age > max_age_secs {
            return Err(WireError::TimestampExpired {
                age_secs: age,
                max_secs: max_age_secs,
            });
        }

        Ok(())
    }

    /// Serialise the envelope to bytes (fixed 200-byte encoding).
    pub fn to_bytes(&self) -> [u8; ENVELOPE_SIZE] {
        let mut buf = [0u8; ENVELOPE_SIZE];
        buf[0..136].copy_from_slice(&self.signable_bytes());
        buf[136..200].copy_from_slice(&self.signature);
        buf
    }

    /// Deserialise an envelope from bytes.
    ///
    /// **Warning**: the returned envelope is *unverified*.  Callers MUST call
    /// [`verify()`](Self::verify) before trusting any field.  Prefer
    /// [`from_bytes_verified()`](Self::from_bytes_verified) for production use.
    pub fn from_bytes(data: &[u8; ENVELOPE_SIZE]) -> Self {
        let mut nonce = [0u8; 32];
        let mut peer_agent_id = [0u8; 32];
        let mut attestation_hash = [0u8; 32];
        let mut chain_root_hash = [0u8; 32];
        let mut ts_bytes = [0u8; 8];
        let mut signature = [0u8; 64];

        nonce.copy_from_slice(&data[0..32]);
        peer_agent_id.copy_from_slice(&data[32..64]);
        attestation_hash.copy_from_slice(&data[64..96]);
        chain_root_hash.copy_from_slice(&data[96..128]);
        ts_bytes.copy_from_slice(&data[128..136]);
        signature.copy_from_slice(&data[136..200]);

        Self {
            nonce,
            peer_agent_id,
            attestation_hash,
            chain_root_hash,
            timestamp: u64::from_le_bytes(ts_bytes),
            signature,
            verified: false,
        }
    }

    /// Deserialise **and** verify an envelope in a single step.
    ///
    /// This is the recommended entry-point for production code.  It
    /// combines [`from_bytes()`](Self::from_bytes) with
    /// [`verify()`](Self::verify) and marks the result as verified.
    #[allow(clippy::too_many_arguments)]
    pub fn from_bytes_verified(
        data: &[u8; ENVELOPE_SIZE],
        expected_own_id: &[u8; 32],
        expected_nonce: Option<&[u8; 32]>,
        attestation: &GeometricAttestation,
        chain_anchor: Option<&GeometricAttestation>,
        signer_pk: &VerifyingKey,
        now: u64,
        max_age_secs: u64,
    ) -> Result<Self, WireError> {
        let mut env = Self::from_bytes(data);
        env.verify(
            expected_own_id,
            expected_nonce,
            attestation,
            chain_anchor,
            signer_pk,
            now,
            max_age_secs,
        )?;
        env.verified = true;
        Ok(env)
    }

    /// Returns `true` if this envelope was produced by [`create()`](Self::create)
    /// or successfully passed through [`from_bytes_verified()`](Self::from_bytes_verified).
    pub fn is_verified(&self) -> bool {
        self.verified
    }
}

/// Short hex for error messages.
fn hex_short(bytes: &[u8; 32]) -> String {
    bytes[..4]
        .iter()
        .map(|b| format!("{b:02x}"))
        .collect::<String>()
        + "..."
}

